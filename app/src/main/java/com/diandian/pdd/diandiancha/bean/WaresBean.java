public class WaresBean {
	private int id;
	private String waresName;
	private String waresPrice;
	private String waresImgName;
	private String sellerName;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getWaresName() {
		return waresName;
	}
	public void setWaresName(String waresName) {
		this.waresName = waresName;
	}
	public String getWaresPrice() {
		return waresPrice;
	}
	public void setWaresPrice(String waresPrice) {
		this.waresPrice = waresPrice;
	}
	public String getWaresImgName() {
		return waresImgName;
	}
	public void setWaresImgName(String waresImgName) {
		this.waresImgName = waresImgName;
	}
	public String getSellerName() {
		return sellerName;
	}
	public void setSellerName(String sellerName) {
		this.sellerName = sellerName;
	}
	
}
